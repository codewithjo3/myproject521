                It was in the year 1986 that the University of Mysore with the support of DRDO, Ministry of Defence, Government of India, launched a very new Post Graduate programme in Computer Science called M.Sc. (Computer Science) mainly to cater to the demands of good software engineering in DRDO labs all over the nation. B.Sc. candidates drawn from all over India, selected through an entrance examination, were given DRDO scholarships and absorbed subsequently after the completion of their Masters degree. After the completion of this agreement period of 5 years, the course was taken over and is still being run by the University of Mysore as per the University norms. Since the inception of Master degree in Computer Science in 1986 to the year 1991 – 92, it was functioning as a Post Graduation department. The department was upgraded to the status of Department of Studies in Computer Science in the year 1991-92. MCA (Master of Computer Application) was added in the year 1992 – 93. The graduates have been doing very well in Research Labs, Industries, Multinational Companies in India and abroad. The department is committed to bring quality in all the academic programmes.

                The University of Mysore is proud that it was the very first University in the State of Karnataka in instituting an exclusive department of Post Graduation Studies and Research in Computer Science. The ambition of the department is to provide the quality education and practical training and conduct the research of international standards and repute.
                
                 RESEARCH PROGRAMMES
                 The Department, in principle, aims to develop research activities all the areas of computer science and technology. Presently Computer Cognition Technology is identified as the thrust area of research.
                
                 The department is very well known at National and International levels for the research activities in Pattern Recognition, Image Processing, Computer Vision, Character Recognition and Analysis, Artificial Intelligence, Symbolic Data Analysis, Dimensionality Reduction, Object Recognition, Pitman Shorthand Recognition, Multilingual Recognition.
                
                 Since 1.1.1997, 26 candidates have got their Ph. D. degrees and 10 candidates have completed their M. Sc. Tech. (by research). Right now, 28 candidates are working towards their doctoral degree. About 300 papers produced, shows the commitment of the department towards quality research. The department is also actively involved in conduction of International level conferences such as of NCDAR 2001, NCDAR 2003, ICCR 2005, ICCR 2008, ICSIP 2009.
                
                 The Department has been carrying out research projects sponsored by various funding agencies like DST, UGC, AICTE and ISRO.
                
                 The department is equipped with an exclusive research laboratory with all the infrastructure facility.
                
              
